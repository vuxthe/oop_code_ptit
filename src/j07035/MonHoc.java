package j07035;

public class MonHoc {
    private String ma, ten;
    private int sl;

    public MonHoc(String ma, String ten, int sl) {
        this.ma = ma;
        this.ten = ten;
        this.sl = sl;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }
}
