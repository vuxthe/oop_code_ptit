package j07019;

public class MatHang {
    private String ma, ten;
    private int gia1, gia2;

    public MatHang(String ma, String ten, int gia1, int gia2) {
        this.ma = ma;
        this.ten = ten;
        this.gia1 = gia1;
        this.gia2 = gia2;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }

    public int getGia1() {
        return gia1;
    }

    public int getGia2() {
        return gia2;
    }
}
